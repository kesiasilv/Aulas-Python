
## formatando o tipo de dado
x = input('digite um numero: ')
print(type(x)) #mostra o tipo
y = int(x) + 1 #depois de converter o tipo realiza a soma
print(y)

x = input('digite outro numero: ')
z = float(x) + 1 #depois de converter o tipo realiza a soma
print(z)

w = int(input('digite mais um numero: ')) ## aqui ja formata o dado
print(w)

n = input('digite seu nome: ')
